uint32_t IM1275_Cal_Current(uint8_t *buff)
{
    uint32_t val = 0;

    val  = (buff[0]) << 24;
    val |= (buff[1]) << 16;
    val |= (buff[2]) << 8;
    val |= (buff[3]) << 0;
	
	return val;
}
	
